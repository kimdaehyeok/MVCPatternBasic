    {
        return hiredate;
    }
    public void setHiredate(java.util.Date hiredate)
    {
        this.hiredate = hiredate;
    }
    public int getSal()
    {
        return sal;
    }
    public void setSal(int sal)
    {
        this.sal = sal;
    }
