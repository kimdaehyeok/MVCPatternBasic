    {
        return comm;
    }
    public void setComm(int comm)
    {
        this.comm = comm;
    }
    public int getDeptno()
    {
        return deptno;
    }
    public void setDeptno(int deptno)
    {
        this.deptno = deptno;
    }
    public String getId()
    {
        return id;
    }
    public void setId(String id)
    {
        this.id = id;
    }
    public String getPassword()
    {
        return password;
    }
    public void setPassword(String password)
    {
        this.password = password;
    }
